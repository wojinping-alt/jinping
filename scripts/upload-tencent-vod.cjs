/* eslint-disable @typescript-eslint/no-require-imports */
const fs = require("fs");
const path = require("path");
const { VodUploadClient, VodUploadRequest } = require("vod-node-sdk");

const root = path.resolve(__dirname, "..");
const envPath = path.join(root, "vod-upload.env.local");
const resultsPath = path.join(root, "vod-upload-results.json");
const sqlPath = path.join(root, "supabase-vod-course-data.sql");

function loadLocalEnv() {
  if (!fs.existsSync(envPath)) return;

  const content = fs.readFileSync(envPath, "utf8");
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const index = trimmed.indexOf("=");
    if (index === -1) continue;
    const key = trimmed.slice(0, index).trim();
    const value = trimmed.slice(index + 1).trim();
    if (key && !process.env[key]) process.env[key] = value;
  }
}

function requireEnv(name) {
  const value = process.env[name];
  if (!value) {
    throw new Error(
      `Missing ${name}. Put it in ${path.basename(envPath)} or export it before running.`
    );
  }
  return value;
}

function getEpisodeNumber(fileName) {
  const match = fileName.match(/(?:Q\d)?第\s*(\d+)\s*集/i);
  return match ? Number(match[1]) : Number.MAX_SAFE_INTEGER;
}

function getVideos(dir) {
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && /\.mp4$/i.test(entry.name))
    .map((entry) => ({
      name: entry.name,
      fullPath: path.join(dir, entry.name),
      episodeNumber: getEpisodeNumber(entry.name),
    }))
    .sort((a, b) => a.episodeNumber - b.episodeNumber || a.name.localeCompare(b.name, "zh-Hans-CN"));
}

function uploadOne(client, region, request) {
  return new Promise((resolve, reject) => {
    client.upload(region, request, (error, data) => {
      if (error) reject(error);
      else resolve(data);
    });
  });
}

function sqlString(value) {
  return String(value ?? "").replace(/'/g, "''");
}

function buildSql(results) {
  const lines = [
    "-- Generated by scripts/upload-tencent-vod.cjs",
    "-- Run supabase-course-episodes.sql before this file.",
    "",
  ];

  for (const course of results.courses) {
    const firstUrl = course.episodes[0]?.mediaUrl || "";
    lines.push(
      `update public.courses set video_url = '${sqlString(firstUrl)}' where id = ${course.courseId};`
    );

    for (const episode of course.episodes) {
      lines.push(
        [
          "insert into public.course_episodes",
          "(course_id, episode_number, title, video_url, vod_file_id)",
          "values",
          `(${course.courseId}, ${episode.episodeNumber}, '${sqlString(episode.title)}', '${sqlString(
            episode.mediaUrl
          )}', '${sqlString(episode.fileId)}')`,
          "on conflict (course_id, episode_number) do update set",
          "title = excluded.title,",
          "video_url = excluded.video_url,",
          "vod_file_id = excluded.vod_file_id;",
        ].join(" ")
      );
    }

    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

async function main() {
  loadLocalEnv();

  const secretId = requireEnv("TENCENTCLOUD_SECRET_ID");
  const secretKey = requireEnv("TENCENTCLOUD_SECRET_KEY");
  const region = process.env.TENCENT_VOD_REGION || "ap-guangzhou";
  const storageRegion = process.env.TENCENT_VOD_STORAGE_REGION || "ap-guangzhou";

  const courses = [
    {
      courseId: 101,
      code: "Q1",
      title: "Q1 汉字课程合集",
      dir: "D:\\批量视频\\20集\\Q1",
    },
    {
      courseId: 102,
      code: "Q2",
      title: "Q2 汉字课程合集",
      dir: "D:\\批量视频\\网站开发\\合并视频\\q2",
    },
  ];

  const client = new VodUploadClient(secretId, secretKey);
  const uploaded = fs.existsSync(resultsPath)
    ? JSON.parse(fs.readFileSync(resultsPath, "utf8"))
    : { courses: [] };

  for (const course of courses) {
    if (!fs.existsSync(course.dir)) {
      throw new Error(`Video folder does not exist: ${course.dir}`);
    }

    let target = uploaded.courses.find((item) => item.courseId === course.courseId);
    if (!target) {
      target = { courseId: course.courseId, code: course.code, title: course.title, episodes: [] };
      uploaded.courses.push(target);
    }

    for (const video of getVideos(course.dir)) {
      const existing = target.episodes.find((item) => item.filePath === video.fullPath);
      if (existing?.fileId && existing?.mediaUrl) {
        console.log(`skip ${course.code} 第${video.episodeNumber}集: ${existing.fileId}`);
        continue;
      }

      const req = new VodUploadRequest();
      req.MediaFilePath = video.fullPath;
      req.MediaName = `${course.code} 第${video.episodeNumber}集`;
      if (storageRegion) req.StorageRegion = storageRegion;

      console.log(`upload ${course.code} 第${video.episodeNumber}集: ${video.fullPath}`);
      const data = await uploadOne(client, region, req);
      if (!data.FileId || !data.MediaUrl) {
        throw new Error(`Upload did not return FileId/MediaUrl: ${JSON.stringify(data)}`);
      }

      target.episodes.push({
        episodeNumber: video.episodeNumber,
        title: `${course.code} 第${video.episodeNumber}集`,
        fileName: video.name,
        filePath: video.fullPath,
        fileId: data.FileId,
        mediaUrl: data.MediaUrl,
      });

      target.episodes.sort((a, b) => a.episodeNumber - b.episodeNumber);
      fs.writeFileSync(resultsPath, `${JSON.stringify(uploaded, null, 2)}\n`, "utf8");
      fs.writeFileSync(sqlPath, buildSql(uploaded), "utf8");
    }
  }

  fs.writeFileSync(resultsPath, `${JSON.stringify(uploaded, null, 2)}\n`, "utf8");
  fs.writeFileSync(sqlPath, buildSql(uploaded), "utf8");
  console.log(`done: ${resultsPath}`);
  console.log(`sql: ${sqlPath}`);
}

main().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});
